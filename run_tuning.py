import os
import datetime
import argparse
import json


# Load config options
parser = argparse.ArgumentParser()
parser.add_argument("--dataset", type=str, default="caltech101", nargs='*', help="Dataset name")
parser.add_argument("--trainer", type=str, default="CoOp",
                    help="Name of trainer, e.g, CoOp, FVGPT_CoOp")
parser.add_argument("--seed_list", type=int, default=1, nargs='*', help="A list of seeds")
parser.add_argument("--sub_class", type=str, default="new", help="For BASE or NEW class")
parser.add_argument("--add_opts", type=str, default="", help="Additional options")

# [FVGPT] New Configs
parser.add_argument("--lambda_base", type=float, default=1.0, help="cfg.TRAINER.AMPT.RDBOOST_LAMBDA_D")
parser.add_argument("--lambda_new", type=float, default=1.0, help="cfg.TRAINER.AMPT.BDPC_LAMBDA")
args = parser.parse_args()


datasets = args.dataset
trainer = args.trainer  # backbone models (CoOp / KgCoOp / PromptSRC / MMRL)
seed_list = args.seed_list
subsample_class = args.sub_class  # base / new / all

lambda_base = args.lambda_base
lambda_new = args.lambda_new
add_opts = args.add_opts  # Optional extra arguments for overriding, e.g., "[[arg1,new_val1],[arg2,new_val2],...]"; wrap the nested list in quotes.


# Given a nested-list string like "[[arg1,new_val1],[arg2,new_val2],...]", first parse it back into a nested list, then use it to override the original values passed by the main program
def opt_override(opt_list):
    if opt_list != "":
        nested_list = json.loads(opt_list)
        for arg_and_value in nested_list:
            globals()[arg_and_value[0]] = arg_and_value[1]


# If log.txt already exists before training, rename the existing file to avoid conflicts with the log.txt generated by this training/fine-tuning run.
def detect_and_rename_existing_log(output_dir):
    log_txt = output_dir + "/log.txt"
    if os.path.exists(log_txt):
        time_for_name = datetime.datetime.now().strftime("%Y-%m-%d-%H-%M-%S")
        new_log_txt = output_dir + "/log-" + str(time_for_name) + ".txt"
        os.rename(log_txt, new_log_txt)


def run_and_extract_result(command, output_dir):
    result_lines = []
    if "ERROR" in command:
        result_lines = ["ERROR \n", "ERROR \n", "ERROR \n", "ERROR \n", "ERROR \n"]
    else:
        os.system(command)  # Run the command
        log_txt = output_dir + "/log.txt"  # After train/val completes, read log.txt in the corresponding directory
        if os.path.exists(log_txt):
            with open(log_txt, "r") as f:
                lines = f.readlines()
                result_lines = lines[-6:]  # Extract the last 6 lines (total/correct/acc, etc.)
    return result_lines


