def get_trainer_configs(target_trainer):
    config_filex = ""
    baseline_epoch = 0
    baseline_backbone = ""

    if target_trainer == "CoOp" or target_trainer == "FVGPT_CoOp":
        config_filex = "vit_b16_ep10"
        baseline_epoch = 10
        baseline_backbone = "prompt_learner"
    elif target_trainer == "PromptSRC" or target_trainer == "FVGPT_PromptSRC":
        config_filex = "vit_b16_c2_ep10_batch32_4+4ctx"
        baseline_epoch = 10
        baseline_backbone = "VLPromptLearner"
    elif target_trainer == "KgCoOp" or target_trainer == "FVGPT_KgCoOp":
        config_filex = "vit_b16_ep10_ctxv1_bs4_lr35"
        baseline_epoch = 10
        baseline_backbone = "prompt_learner"
    elif target_trainer == "MMRL" or target_trainer == "FVGPT_MMRL":
        config_filex = "vit_b16"
        baseline_epoch = 10
        baseline_backbone = "MultiModalRepresentationLearner"

    return config_filex, baseline_epoch, baseline_backbone


def basic_paths(dataset, trainer):
    config_filex, _, _ = get_trainer_configs(trainer)
    config_file = "configs/trainers/" + trainer + "/" + config_filex + ".yaml"
    base_output_dir = "output/" + trainer + "/" + dataset + "/base/" + config_filex  # [Note] seed is not included
    new_output_dir = "output/" + trainer + "/" + dataset + "/new/" + config_filex  # [Note] seed is not included
    return config_file, base_output_dir, new_output_dir


# Start loop
for dataset in datasets:
    # Load all config options
    part_config_param, epoch, backbone_name = get_trainer_configs(trainer)
    root, dataset_config_file = get_dataset_config(dataset)
    config_file, base_output_dir, new_output_dir = basic_paths(dataset, trainer)

    opt_override(add_opts)

    # Build the command for each seed
    for seed in seed_list:
        # Build required paths; for FVGPT experiments, append hyperparameters to the directory for ablation studies
        if "FVGPT" in base_output_dir:
            full_base_path = base_output_dir + "-b" + str(lambda_base) + "-n" + str(lambda_new) + "/seed" + str(seed)
            ckpt_path = base_output_dir + "-b" + str(lambda_base) + "-n" + str(lambda_new) + "/seed" + str(seed) + "/" + backbone_name  # Used to check whether the checkpoint exists
            full_new_path = new_output_dir + "-b" + str(lambda_base) + "-n" + str(lambda_new) + "/seed" + str(seed)
        else:
            full_base_path = base_output_dir + "/seed" + str(seed)
            ckpt_path = base_output_dir + "/seed" + str(seed) + "/" + backbone_name  # Used to check whether the checkpoint exists
            full_new_path = new_output_dir + "/seed" + str(seed)

        final_output_dir = ""
        command = ""

        # Check whether a checkpoint exists
        if os.path.exists(ckpt_path):
            if subsample_class == "base":
                report = trainer.upper() + " Checkpoint detected (" + ckpt_path + ") in training phase, turning into val on [BASE] class"
                # If a previous log.txt exists in the val output directory, rename it
                detect_and_rename_existing_log(full_base_path)
                final_output_dir = full_base_path
                # Build the same command as base-class training, and run inference
                command = ("python train.py --root " + root + " --seed " + str(seed) + " --trainer " + trainer
                           + " --dataset-config-file " + dataset_config_file + " --config-file " + config_file
                           + " --output-dir " + full_base_path
                           + " DATASET.NUM_SHOTS 16"
                           + " TRAINER.COOP.N_CTX 4 TRAINER.COOP.CSC False TRAINER.COOP.CLASS_TOKEN_POSITION end"
                           + " TRAINER.AMPT.RDBOOST_LAMBDA_D " + str(lambda_base)
                           + " TRAINER.AMPT.BDPC_LAMBDA " + str(lambda_new)
                           + " DATASET.SUBSAMPLE_CLASSES " + subsample_class
                           )
            elif subsample_class == "new":
                report = trainer.upper() + " Checkpoint detected (" + ckpt_path + "), turning into val phase on [NEW] or [ALL] class"
                # If a previous log.txt exists in the val output directory, rename it
                detect_and_rename_existing_log(full_new_path)
                final_output_dir = full_new_path
                # Build inference command
                command = ("python train.py  --root " + root + " --seed " + str(seed) + " --trainer " + trainer
                           + " --dataset-config-file " + dataset_config_file + " --config-file " + config_file
                           + " --output-dir " + full_new_path + " --model-dir " + full_base_path
                           + " --load-epoch " + str(epoch) + " --eval-only DATASET.NUM_SHOTS 16"
                           + " TRAINER.COOP.N_CTX 4 TRAINER.COOP.CSC False TRAINER.COOP.CLASS_TOKEN_POSITION end"
                           + " TRAINER.AMPT.RDBOOST_LAMBDA_D " + str(lambda_base)
                           + " TRAINER.AMPT.BDPC_LAMBDA " + str(lambda_new)
                           + " DATASET.SUBSAMPLE_CLASSES " + subsample_class
                           )
            else:
                report = "ERROR!! This script only support BASE or NEW mode!"

            print("[FVGPT_TUNING_SCRIPT]: " + report)

        # If the expected path does not exist, run standard base-class training/inference
        else:
            final_output_dir = ""
            if subsample_class == "base":
                report = "Start training on BASELINE"
                print("[FVGPT_TUNING_SCRIPT]: " + report)
                detect_and_rename_existing_log(full_base_path)
                final_output_dir = full_base_path
                # Build the same command as base-class training, and run inference
                command = ("python train.py  --root " + root + " --seed " + str(seed) + " --trainer " + trainer
                           + " --dataset-config-file " + dataset_config_file + " --config-file " + config_file
                           + " --output-dir " + full_base_path
                           + " DATASET.NUM_SHOTS 16"
                           + " TRAINER.COOP.N_CTX 4 TRAINER.COOP.CSC False TRAINER.COOP.CLASS_TOKEN_POSITION end"
                           + " TRAINER.AMPT.RDBOOST_LAMBDA_D " + str(lambda_base)
                           + " TRAINER.AMPT.BDPC_LAMBDA " + str(lambda_new)
                           + " DATASET.SUBSAMPLE_CLASSES " + subsample_class
                           )
            elif subsample_class == "new":
                report = "ERROR!! CANNOT INFERENCE ON [NEW] CLASS WITHOUT [BASE] CHECKPOINT!!"
                print("[FVGPT_TUNING_SCRIPT]: " + report)
                command = "ERROR"
            else:
                report = "ERROR!! This script only support BASE or NEW mode!"
                print("[FVGPT_TUNING_SCRIPT]: " + report)
                command = "ERROR"

        # print("command: ", command, "\n final_output_dir: ", final_output_dir)  # [Debug]

        print("COMMAND: ", command)
        result = run_and_extract_result(command, final_output_dir)
        acc_info = report + "\n"
        for item in result:
            acc_info = acc_info + item
        time_now = datetime.datetime.now().strftime("%Y-%m-%d %H:%M:%S")
        result_info = ("[Time] " + str(time_now) + "\n === Results of " + trainer
                       + " on [" + subsample_class + "] setting: "
                       + dataset + ", " + config_file + ", seed " + str(seed) + " ==="
                       + "\n === lambda_base: " + str(lambda_base) + ", lambda_new: " + str(lambda_new) + " === \n"
                       + acc_info)

        with open("output/" + trainer + "/recorded_experiments.txt", "a") as file:
            file.write(result_info + "\n")
